package com.proyectoinventario.ProyectoInventario.model;

public class PedidoSalida {

    //atributos
    private Long idPedidoSalida;
    private String destino;

    //metodos getter and setter

    public Long getIdPedidoSalida() {
        return idPedidoSalida;
    }

    public void setIdPedidoSalida(Long idPedidoSalida) {
        this.idPedidoSalida = idPedidoSalida;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

}
